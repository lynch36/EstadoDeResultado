    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ventas = Integer.parseInt(jTextField1.getText());
        
        int devVenta = Integer.parseInt(jTextField2.getText());
        int desVenta = Integer.parseInt(jTextField3.getText());
        int ventasNetas = (ventas-devVenta-desVenta);
        ventasN = String.valueOf(ventasNetas);
        
        int cTotal = Integer.parseInt(jTextField4.getText());
        int gastoCompra = Integer.parseInt(jTextField5.getText());
        int cBrutas = (cTotal+gastoCompra);
        
        int devCompra = Integer.parseInt(jTextField6.getText());
        int desCompra = Integer.parseInt(jTextField7.getText());
        int cNetas = (cBrutas-devCompra-desCompra);
        
        int invInicial = Integer.parseInt(jTextField8.getText());
        int invFinal = Integer.parseInt(jTextField9.getText());
        int cVentas = (cNetas+invInicial-invFinal);
        
        costoVentas = String.valueOf(cVentas);
        
        int utilBruta = (ventasNetas-cVentas);
        
        int gVentas = Integer.parseInt(jTextField10.getText());
        int gAdmin = Integer.parseInt(jTextField11.getText());
        int uOperaciones = (utilBruta-gVentas-gAdmin);
        
        gastoVentas = String.valueOf(gVentas);
        gastoAdmin = String.valueOf(gAdmin);
        utilidadOperaciones = String.valueOf(uOperaciones);
        
        int gFin = Integer.parseInt(jTextField12.getText());
        int pFin = Integer.parseInt(jTextField13.getText());
        int oGastos = Integer.parseInt(jTextField14.getText());
        int oProductos = Integer.parseInt(jTextField15.getText());
        int uImpuesto = (uOperaciones-gFin+pFin-oGastos+oProductos);
        
        gastoFin = String.valueOf(gFin);
        productoFin = String.valueOf(pFin);
        otroGastos = String.valueOf(oGastos);
        otroProductos = String.valueOf(oProductos);
        utilImpuesto = String.valueOf(uImpuesto);
        
        Pantalla4 obj = new Pantalla4();
        obj.setVisible(true);
        dispose();        
    }//GEN-LAST:event_jButton1ActionPerformed

